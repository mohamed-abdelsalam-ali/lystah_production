    public function save_user_role(Request $request)
    {

        // return $request;
        if($request->flagCheckbox==1)
        {
            $user=User::where('id',$request->userID)->first();
            // return $user;
            $user->assignRole($request->input('roleID'));
        }else{
            UserRole::where('model_id','=',$request->userID)->where('role_id',$request->roleID)->delete();
        }

